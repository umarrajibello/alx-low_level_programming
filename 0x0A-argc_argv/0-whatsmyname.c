#include <stdio.h>

/**
 * main - program to use environment variables argc and  argv.
 *@argc: the cout of environment variables.
 *@argv: an array of names of input variables.
 *
 * Return: Always 0.
 */
int main(int __attribute__((__unused__)) argc, char *argv[])
{
	printf("%s\n", argv[0]);
	return (0);
}
